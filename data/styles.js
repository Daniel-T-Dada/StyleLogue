export const getStylesByCategory = (categoryId) => {
    return styles.filter((style) => style.category === categoryId);
};

export const getTrendingStyles = () => {
    return styles.filter((style) => style.trending);
};

export const getStyleById = (id) => {
    return styles.find((style) => style.id === id);
};

export const searchStyles = (query) => {
    const lowercaseQuery = query.toLowerCase();
    return styles.filter(
        (style) =>
            style.title.toLowerCase().includes(lowercaseQuery) ||
            style.category.toLowerCase().includes(lowercaseQuery) ||
            style.tags.some((tag) => tag.toLowerCase().includes(lowercaseQuery))
    );
};
